e=8.85*10^-12;
sep=0.25*10^-6;
D=1.211*10^-7;
P=1:1:25;
PN=P.^-1
r=1250*10^-5;
%Circle
x=(r^2*sqrt(P)+8*sqrt(sep*D))/(r^2*sqrt(P)-8*sqrt(sep*D));
y=PN*D/sep
Cc=4*10^12*pi*e*sqrt(y)*log(x)
%ellipse
a=500*10^-5; 
b=400*10^-5;    
h=a^4*b^4/(3*a^2+3*b^2+2*a^2*b^2);
W1=P*h/(8*D);
W1IN=W1.^-1/2;
Ae=pi*a*b;
z=W1IN*0.5*sep^-1/2;
f=(sqrt(sep)+sqrt(W1))/(sqrt(sep)-sqrt(W1));
Ce=e*Ae*z*log(f)
%square
s=32*10^-4;
t=1*s^4/sep;
q=12.5*P*s^4;
Cs=t*(1+(q)/(2015*D*sep)) 
plot(P,Cs)
legend('Circle','Square','ellipse')
